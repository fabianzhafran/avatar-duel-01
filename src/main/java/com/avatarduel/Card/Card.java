abstract class Card {

    protected String name;
    protected Element element;
    protected String description;
    protected String pathGambar;



    public String getNama() {
        return name;
    }

    public Element getElement() {
        return element;
    }

    public String getDeskripsi() {
        return description;
    }

    public String pathGambar() {
        return pathGambar;
    }

}